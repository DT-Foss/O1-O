# ─── Mission Package Builder ────────────────────────────────────────

class MissionPackageBuilder:
    """Assemble complete operation packages from FORGE outputs."""

    def __init__(self, work_dir: str = '/tmp/forge_mission'):
        self.work_dir = work_dir
        self.packages_built = 0

    def build(self, mission: dict) -> dict:
        """Build a complete mission package.

        Args:
            mission: {
                name: str,
                target: {name, platform, path, port, process},
                payload_code: str,         # Primary payload
                variants: List[str],       # Mutated variants
                kill_chain: List[str],     # Kill chain steps
                findings: List[dict],      # Vulnerability findings
                techniques: List[str],     # Evasion techniques used
                artifacts: List[str],      # Files to clean up
            }

        Returns:
            {package_dir, manifest, files_created, archive_path}
        """
        self.packages_built += 1
        timestamp = datetime.now(timezone.utc).strftime('%Y%m%d_%H%M%S')
        mission_name = mission.get('name', f'mission_{self.packages_built}')
        safe_name = mission_name.replace(' ', '_').lower()

        pkg_dir = os.path.join(self.work_dir, f"{safe_name}_{timestamp}")
        os.makedirs(pkg_dir, exist_ok=True)

        target = mission.get('target', {})
        files_created = []
        manifest = {
            'name': mission_name,
            'created': datetime.now(timezone.utc).isoformat(),
            'target': target,
            'files': {},
            'checksums': {},
        }

        # 1. Payload directory
        payload_dir = os.path.join(pkg_dir, 'payload')
        os.makedirs(payload_dir, exist_ok=True)

        if mission.get('payload_code'):
            primary_path = os.path.join(payload_dir, 'primary.py')
            with open(primary_path, 'w') as f:
                f.write(mission['payload_code'])
            files_created.append('payload/primary.py')
            manifest['checksums']['payload/primary.py'] = hashlib.sha256(
                mission['payload_code'].encode()
            ).hexdigest()

        for i, variant in enumerate(mission.get('variants', []), 1):
            var_code = variant if isinstance(variant, str) else variant.get('variant', variant.get('code', ''))
            var_path = os.path.join(payload_dir, f'variant_{i}.py')
            with open(var_path, 'w') as f:
                f.write(var_code)
            files_created.append(f'payload/variant_{i}.py')
            manifest['checksums'][f'payload/variant_{i}.py'] = hashlib.sha256(
                var_code.encode()
            ).hexdigest()

        # 2. Validation script
        validate_code = _generate_validation_script(target)
        validate_path = os.path.join(pkg_dir, 'validate.py')
        with open(validate_path, 'w') as f:
            f.write(validate_code)
        files_created.append('validate.py')

        # 3. Cleanup script
        cleanup_code = _generate_cleanup_script(target, mission.get('artifacts', []))
        cleanup_path = os.path.join(pkg_dir, 'cleanup.py')
        with open(cleanup_path, 'w') as f:
            f.write(cleanup_code)
        files_created.append('cleanup.py')

        # 4. OpSec checklist
        opsec = _generate_opsec_checklist(target, mission.get('techniques', []))
        opsec_path = os.path.join(pkg_dir, 'opsec_checklist.md')
        with open(opsec_path, 'w') as f:
            f.write(opsec)
        files_created.append('opsec_checklist.md')

        # 5. Reports
        report_dir = os.path.join(pkg_dir, 'report')
        os.makedirs(report_dir, exist_ok=True)

        if mission.get('findings'):
            from o1o_o.core.report_generator import ReportGenerator
            gen = ReportGenerator(analyst='FORGE', organization=mission.get('organization', ''))
            gen.generate_all_reports(mission['findings'])

            for report in gen.reports:
                md_path = os.path.join(report_dir, f'{report.id}.md')
                with open(md_path, 'w') as f:
                    f.write(gen.to_markdown(report))
                files_created.append(f'report/{report.id}.md')

            sarif_path = os.path.join(report_dir, 'forge_results.sarif')
            with open(sarif_path, 'w') as f:
                json.dump(gen.to_sarif(), f, indent=2)
            files_created.append('report/forge_results.sarif')

        # 6. README
        readme = _generate_readme(mission)
        readme_path = os.path.join(pkg_dir, 'README.md')
        with open(readme_path, 'w') as f:
            f.write(readme)
        files_created.append('README.md')

        # 7. Manifest
        manifest['files'] = files_created
        manifest['file_count'] = len(files_created)
        manifest_path = os.path.join(pkg_dir, 'manifest.json')
        with open(manifest_path, 'w') as f:
            json.dump(manifest, f, indent=2)
        files_created.append('manifest.json')

        # 8. Create archive
        archive_path = f"{pkg_dir}.tar.gz"
        with tarfile.open(archive_path, 'w:gz') as tar:
            tar.add(pkg_dir, arcname=os.path.basename(pkg_dir))

        return {
            'package_dir': pkg_dir,
            'archive_path': archive_path,
            'manifest': manifest,
            'files_created': files_created,
            'archive_size': os.path.getsize(archive_path),
        }

    def build_from_forge_output(self, code: str, task_name: str = '',
                                 platform: str = 'linux') -> dict:
        """Build a mission package from FORGE-generated code.

        Convenience method that auto-generates variants and reports.
        """
        # Generate variants
        try:
            from o1o_o.core.mutation_engine import MutationEngine
            me = MutationEngine()
            variants = me.generate_variants(code, count=3, level=3)
        except Exception:
            variants = []

        # Detect and report
        findings = []
        try:
            from o1o_o.core.detection_test import DetectionEngine
            de = DetectionEngine()
            scan = de.scan(code)
            if not scan.clean:
                for det in scan.detections:
                    findings.append({
                        'primitive_type': det['category'],
                        'severity': det['severity'],
                        'target_name': task_name or 'payload',
                        'faulting_function': det['rule'],
                        'platform': platform,
                    })
        except Exception:
            pass

        mission = {
            'name': task_name or 'FORGE Operation',
            'target': {
                'name': task_name or 'target',
                'platform': platform,
            },
            'payload_code': code,
            'variants': [v.get('variant', '') for v in variants],
            'findings': findings,
            'techniques': [],
            'artifacts': [],
        }

        return self.build(mission)

    def format_report(self, result: dict) -> str:
        """Format package build result."""
        lines = [
            f"MISSION PACKAGE BUILT",
            f"{'=' * 50}",
            f"  Directory:  {result['package_dir']}",
            f"  Archive:    {result['archive_path']}",
            f"  Size:       {result['archive_size']:,} bytes",
            f"  Files:      {len(result['files_created'])}",
            f"",
        ]
        for f in result['files_created']:
            lines.append(f"    {f}")

        return '\n'.join(lines)
