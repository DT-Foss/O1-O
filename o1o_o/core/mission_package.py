def _generate_validation_script(target: dict) -> str:
    """Generate target validation script (pre-flight check)."""
    name = target.get('name', 'target')
    platform = target.get('platform', 'linux')

    script = f'''#!/usr/bin/env python3
"""Target Validation Script — Pre-flight check for {name}
Generated by FORGE Mission Package Builder
"""
import os
import sys
import socket
import platform as plat

def validate():
    """Run all pre-flight checks. Returns True if target is valid."""
    checks = []

    # Check 1: Platform
    current_os = plat.system().lower()
    expected = "{platform}"
    if expected in ('windows', 'linux', 'macos', 'darwin'):
        ok = (expected in current_os) or (expected == 'macos' and current_os == 'darwin')
        checks.append(('Platform', ok, f'Expected {{expected}}, got {{current_os}}'))

    # Check 2: Architecture
    arch = plat.machine().lower()
    checks.append(('Architecture', True, arch))
'''

    # Add target-specific checks
    if target.get('path'):
        script += f'''
    # Check 3: Target binary exists
    target_path = "{target['path']}"
    exists = os.path.isfile(target_path)
    checks.append(('Target exists', exists, target_path))
'''

    if target.get('port'):
        script += f'''
    # Check 4: Target port reachable
    port = {target['port']}
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        s.settimeout(3)
        result = s.connect_ex(('127.0.0.1', port))
        s.close()
        reachable = result == 0
        checks.append(('Port reachable', reachable, f'Port {{port}}'))
    except Exception as e:
        checks.append(('Port reachable', False, str(e)))
'''

    if target.get('process'):
        script += f'''
    # Check 5: Target process running
    import subprocess
    proc = "{target['process']}"
    try:
        result = subprocess.run(['pgrep', '-f', proc], capture_output=True)
        running = result.returncode == 0
        checks.append(('Process running', running, proc))
    except Exception:
        checks.append(('Process running', False, 'pgrep not available'))
'''

    script += '''
    # Report
    print("=" * 50)
    print("TARGET VALIDATION REPORT")
    print("=" * 50)
    all_pass = True
    for name, ok, detail in checks:
        status = "PASS" if ok else "FAIL"
        if not ok:
            all_pass = False
        print(f"  [{status}] {name}: {detail}")

    print()
    if all_pass:
        print("  ALL CHECKS PASSED — Target is valid")
    else:
        print("  SOME CHECKS FAILED — Review before proceeding")

    return all_pass

if __name__ == '__main__':
    result = validate()
    sys.exit(0 if result else 1)
'''
    return script


def _generate_cleanup_script(target: dict, artifacts: List[str] = None) -> str:
    """Generate cleanup/rollback script."""
    script = '''#!/usr/bin/env python3
"""Cleanup Script — Remove all operation artifacts
Generated by FORGE Mission Package Builder

WARNING: Run this AFTER operation is complete to remove traces.
"""
import os
import sys
import shutil

ARTIFACTS = [
'''

    if artifacts:
        for a in artifacts:
            script += f"    '{a}',\n"

    script += ''']

def cleanup(dry_run=True):
    """Remove operation artifacts.

    Args:
        dry_run: If True, only show what would be deleted
    """
    print("CLEANUP SCRIPT")
    print("=" * 50)

    removed = 0
    for path in ARTIFACTS:
        path = os.path.expanduser(path)
        if os.path.exists(path):
            if dry_run:
                print(f"  [DRY] Would remove: {path}")
            else:
                if os.path.isdir(path):
                    shutil.rmtree(path)
                else:
                    os.unlink(path)
                print(f"  [DEL] Removed: {path}")
            removed += 1
        else:
            print(f"  [---] Not found: {path}")

    # Clean temp files
    import tempfile
    tmp_dir = os.path.join(tempfile.gettempdir(), 'forge_mission')
    if os.path.isdir(tmp_dir):
        if dry_run:
            print(f"  [DRY] Would remove temp dir: {tmp_dir}")
        else:
            shutil.rmtree(tmp_dir)
            print(f"  [DEL] Removed temp dir: {tmp_dir}")
        removed += 1

    print()
    print(f"  {removed} items {'would be ' if dry_run else ''}removed")
    if dry_run:
        print("  Run with --execute to actually remove files")

if __name__ == '__main__':
    dry = '--execute' not in sys.argv
    cleanup(dry_run=dry)
'''
    return script


