"""Shared terminal formatting constants for FORGE modules."""

BOLD    = "\033[1m"
DIM     = "\033[2m"
ITALIC  = "\033[3m"
UNDER   = "\033[4m"
RED     = "\033[91m"
GREEN   = "\033[92m"
YELLOW  = "\033[93m"
BLUE    = "\033[94m"
MAGENTA = "\033[95m"
CYAN    = "\033[96m"
WHITE   = "\033[97m"
GREY    = "\033[90m"
RESET   = "\033[0m"
CLEAR_LINE = "\033[2K"
UP_LINE = "\033[1A"
HIDE_CURSOR = "\033[?25l"
SHOW_CURSOR = "\033[?25h"

def fg256(n): return f"\033[38;5;{n}m"
def bg256(n): return f"\033[48;5;{n}m"
